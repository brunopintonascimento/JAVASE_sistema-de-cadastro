   public void preencherTabela(String sql){
       ArrayList dados  = new ArrayList();
       String [] colunas = new String[]{"ID","nome","cargo","CPF","sexo","TEL","CEL","RG"};
       conecta.Conexao();
       conecta.executaSql(sql);
       
       
       try{
           conecta.rs.first();
           do{
               dados.add(new Object[]{conecta.rs.getString("matricula_funcionario"),conecta.rs.getString("nome_funcionario"),conecta.rs.getString("cargo_funcionario"),
                   conecta.rs.getString("cpf_funcionario"),conecta.rs.getString("sexo_funcionario"),
                   conecta.rs.getString("tel_funcionario"),conecta.rs.getString("cel_funcionario"),conecta.rs.getString("rg_funcionario")});
           }while(conecta.rs.next());
           
       }catch (SQLException ex){
           JOptionPane.showMessageDialog(rootPane,"Erro ao Preencher ArrayList"+ex);
           
       }
       ModeloTabela modelo = new ModeloTabela(dados,colunas);
       TabelaCadastro.setModel(modelo);
       TabelaCadastro.getColumnModel().getColumn(0).setPreferredWidth(35);
       TabelaCadastro.getColumnModel().getColumn(0).setResizable(false);
       TabelaCadastro.getColumnModel().getColumn(1).setPreferredWidth(180);
       TabelaCadastro.getColumnModel().getColumn(1).setResizable(false);
       TabelaCadastro.getColumnModel().getColumn(2).setPreferredWidth(80);
       TabelaCadastro.getColumnModel().getColumn(2).setResizable(false);
       TabelaCadastro.getColumnModel().getColumn(3).setPreferredWidth(80);
       TabelaCadastro.getColumnModel().getColumn(3).setResizable(false);
        TabelaCadastro.getColumnModel().getColumn(4).setPreferredWidth(80);
       TabelaCadastro.getColumnModel().getColumn(4).setResizable(false);
        TabelaCadastro.getColumnModel().getColumn(5).setPreferredWidth(80);
       TabelaCadastro.getColumnModel().getColumn(5).setResizable(false);
        TabelaCadastro.getColumnModel().getColumn(6).setPreferredWidth(80);
       TabelaCadastro.getColumnModel().getColumn(6).setResizable(false);
        TabelaCadastro.getColumnModel().getColumn(7).setPreferredWidth(80);
       TabelaCadastro.getColumnModel().getColumn(7).setResizable(false);
       TabelaCadastro.getTableHeader().setReorderingAllowed(false);
       TabelaCadastro.setAutoResizeMode(TabelaCadastro.AUTO_RESIZE_OFF);
       TabelaCadastro.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
       
       conecta.desconecta();
   }
