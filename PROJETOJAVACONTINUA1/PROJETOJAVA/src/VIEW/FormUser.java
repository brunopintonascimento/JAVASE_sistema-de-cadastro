public void preencherTabela(String sql){
       ArrayList dados  = new ArrayList();
       String [] colunas = new String[]{"ID","nome","Profissao","CPF","sexo","TEL","CEL","RG"};
       conecta.Conexao();
       conecta.executaSql(sql);
       
       
       try{
           conecta.rs.first();
           do{
               dados.add(new Object[]{conecta.rs.getString("usu_cod"),conecta.rs.getString("usu_nome"),conecta.rs.getString("usu_profissao"),
                   conecta.rs.getString("usu_cpf"),conecta.rs.getString("usu_sexo"),
                   conecta.rs.getString("usu_tel"),conecta.rs.getString("usu_cel"),conecta.rs.getString("usu_rg")});
           }while(conecta.rs.next());
           
       }catch (SQLException ex){
          // JOptionPane.showMessageDialog(rootPane,"Erro ao Preencher ArrayList"+ex);
           
       }
       ModeloTabela modelo = new ModeloTabela(dados,colunas);
       jTableUsuario.setModel(modelo);
       jTableUsuario.getColumnModel().getColumn(0).setPreferredWidth(35);
       jTableUsuario.getColumnModel().getColumn(0).setResizable(false);
       jTableUsuario.getColumnModel().getColumn(1).setPreferredWidth(180);
       jTableUsuario.getColumnModel().getColumn(1).setResizable(false);
       jTableUsuario.getColumnModel().getColumn(2).setPreferredWidth(80);
       jTableUsuario.getColumnModel().getColumn(2).setResizable(false);
       jTableUsuario.getColumnModel().getColumn(3).setPreferredWidth(80);
       jTableUsuario.getColumnModel().getColumn(3).setResizable(false);
        jTableUsuario.getColumnModel().getColumn(4).setPreferredWidth(80);
       jTableUsuario.getColumnModel().getColumn(4).setResizable(false);
        jTableUsuario.getColumnModel().getColumn(5).setPreferredWidth(80);
       jTableUsuario.getColumnModel().getColumn(5).setResizable(false);
        jTableUsuario.getColumnModel().getColumn(6).setPreferredWidth(80);
       jTableUsuario.getColumnModel().getColumn(6).setResizable(false);
        jTableUsuario.getColumnModel().getColumn(7).setPreferredWidth(80);
       jTableUsuario.getColumnModel().getColumn(7).setResizable(false);
       jTableUsuario.getTableHeader().setReorderingAllowed(false);
       jTableUsuario.setAutoResizeMode(jTableUsuario.AUTO_RESIZE_OFF);
       jTableUsuario.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
       
       conecta.desconecta();
   }
